    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
          Connection conn =Singleton.getconn() ;
          int idUser = AuthAdmin.idSession; 
        String sql = "SELECT `email` FROM `admin` WHERE id =  '"+idUser+"'  " ;
             

        try {
            System.out.println("req=" + sql);
            Connection cnx = Singleton.getconn();
            Statement stmt = cnx.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            stmt=cnx.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            int cmp = 0;
            while(rs.next()){
                idHolder.put(cmp, rs.getString(1));
                jLabel2.setText(rs.getString(1));
                cmp++;
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_formWindowOpened

